    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        try {
            transaksi trans = new transaksi();
            textTotal.setText("" + trans.total_bayar);
            this.ps = k.getConnection().prepareStatement("insert into transaksi values(?,?,?,?,?,?,?,?)");
            this.ps.setInt(1, 0);
            this.ps.setString(2, trans.nama_pelanggan);
            this.ps.setInt(3, trans.id_menu);
            this.ps.setString(4, trans.tanggal);
            this.ps.setString(5, trans.nama_menu);
            this.ps.setInt(6, trans.harga);
            this.ps.setInt(7, trans.jumlah);
            this.ps.setInt(8, trans.total_bayar);
            int pilihan = JOptionPane.showConfirmDialog(null,
                    "Tanggal: "+trans.tanggal+
                    "\nNama Pelanggan: "+trans.nama_pelanggan+
                    "\nPembelian: "+trans.jumlah+
                    " "+trans.nama_menu+"\nTotal Bayar: "+trans.total_bayar+"\n",
                    "Tambahkan Transaksi? ",JOptionPane.YES_NO_OPTION);
            if (pilihan == JOptionPane.YES_OPTION) {
                this.ps.executeUpdate();
                refreshTable();
            }else if(pilihan == JOptionPane.NO_OPTION){
                refreshTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        try {
            transaksi trans = new transaksi();
            trans.no_transaksi = Integer.parseInt(textNoTransaksi.getText());
            this.ps = k.getConnection().prepareStatement("update transaksi set nama_pelanggan=?,"
                    + "id_menu=?, tgl_transaksi=?, nama_menu=?, harga=?, "
                    + "jumlah=?, total_bayar=? where no_transaksi=?");
            this.ps.setString(1, trans.nama_pelanggan);
            this.ps.setInt(2, trans.id_menu);
            this.ps.setString(3, trans.tanggal);
            this.ps.setString(4, trans.nama_menu);
            this.ps.setInt(5, trans.harga);
            this.ps.setInt(6, trans.jumlah);
            this.ps.setInt(7, trans.total_bayar);
            this.ps.setInt(8, trans.no_transaksi);
            this.ps.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

