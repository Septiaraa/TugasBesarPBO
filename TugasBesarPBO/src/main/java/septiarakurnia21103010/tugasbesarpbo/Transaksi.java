    class transaksi extends Transaksi {

        int no_transaksi, id_menu, harga, jumlah, total_bayar;
        String nama_pelanggan, nama_menu, tanggal;

        public transaksi() { 
            this.nama_pelanggan = textNamaPelanggan.getText();
            String combo = comboIDMenu.getSelectedItem().toString();
            String[] arr = combo.split(":");
            this.id_menu = Integer.parseInt(arr[0]);
            try {
                Date date = textTanggal.getDate();
                DateFormat dateformat = new SimpleDateFormat("YYYY-MM-dd");
                this.tanggal = dateformat.format(date);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            this.nama_menu = arr[1];
            this.harga = Integer.parseInt(arr[2]);
            this.jumlah = Integer.parseInt(textJumlah.getText());
            this.total_bayar = this.harga * this.jumlah;
        }
    }

    public void refreshTable() {
        tabel = new DefaultTableModel();
        tabel.addColumn("No Transaksi");
        tabel.addColumn("Nama Pelanggan");
        tabel.addColumn("ID Menu");
        tabel.addColumn("Tanggal");
        tabel.addColumn("Nama Menu");
        tabel.addColumn("Harga");
        tabel.addColumn("Jumlah");
        tabel.addColumn("Total Bayar");
        transaksiTable.setModel(tabel);
        try {
            this.ps = k.getConnection().prepareStatement("select * from transaksi");
            this.rs = this.ps.executeQuery();
            while (rs.next()) {
                Object[] data = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                    rs.getString(8)
                };
                tabel.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        textNoTransaksi.setText("");
        textNamaPelanggan.setText("");
        textJumlah.setText("");
        textTotal.setText("");
    }

    public void refreshCombo() {
        try {
            this.ps = k.getConnection().prepareStatement("select * from menu where ketersediaan='READY'");
            this.rs = this.ps.executeQuery();
            while (rs.next()) {
                comboIDMenu.addItem(rs.getString("nama_menu") + "(" + rs.getString("harga") + ")");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

