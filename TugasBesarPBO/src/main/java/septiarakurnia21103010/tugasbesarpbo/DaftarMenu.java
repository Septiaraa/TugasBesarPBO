    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        try {
            Menu menu = new Menu();
            this.ps = k.getConnection().prepareStatement("insert into menu values (?,?,?,?)");
            ps.setInt(1, 0);
            ps.setString(2, menu.nama_menu);
            ps.setInt(3, menu.harga);
            ps.setString(4, menu.ketersediaan);
            ps.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_addBtnActionPerformed

