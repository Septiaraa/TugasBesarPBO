    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        try {
            Menu menu = new Menu();
            this.ps = k.getConnection().prepareStatement("update menu set nama_menu=?, harga=?, ketersediaan=? where id_menu=?");
            ps.setString(1, menu.nama_menu);
            ps.setInt(2,menu.harga);
            ps.setString(3, menu.ketersediaan);
            ps.setInt(4, Integer.parseInt(textIDMenu.getText()));
            ps.executeUpdate();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

