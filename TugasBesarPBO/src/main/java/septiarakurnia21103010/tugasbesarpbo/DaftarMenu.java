    public void refreshTable(){
        tabel = new DefaultTableModel();
        tabel.addColumn("ID Menu");
        tabel.addColumn("Nama Menu");
        tabel.addColumn("Harga");
        tabel.addColumn("Ketersediaan");
        menuTable.setModel(tabel);
        try {
            this.ps = k.getConnection().prepareStatement("select * from menu");
            this.rs = this.ps.executeQuery();
            while(rs.next()){
                Object[] data={
                    rs.getInt("id_menu"),
                    rs.getString("nama_menu"),
                    rs.getInt("harga"),
                    rs.getString("ketersediaan")
                };
                tabel.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        textIDMenu.setText("");
        textNamaMenu.setText("");
        textHarga.setText("");
    }
