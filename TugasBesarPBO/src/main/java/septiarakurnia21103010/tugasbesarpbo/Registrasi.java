    class user extends Registrasi{
        int id_user, id_level;
        String username, password, nama_user;

        public user() {
            this.id_level = Integer.parseInt(levelCombo.getSelectedItem().toString());
            //untuk mendapatkan nilai dari combo box harus diconvert dulu ke integer
            this.username = textUsername.getText();
            this.password = textPassword.getText();
            this.nama_user = textNama.getText();
        }
    }
    
    public void refreshTable(){
        tabel = new DefaultTableModel();
        tabel.addColumn("ID User");
        tabel.addColumn("Username");
        tabel.addColumn("Password");
        tabel.addColumn("Nama");
        tabel.addColumn("ID Level");
        registrasiTable.setModel(tabel);
        try {
            this.ps = k.getConnection().prepareStatement("select * from user");
            // untuk eksekusi syntax sql yang disisipkan ke program
            this.rs = this.ps.executeQuery();
            //mengambil data dimasukkan ke gui
            while(rs.next()){
                Object[] data ={
                    rs.getString("id_user"),
                    rs.getString("username"),
                    rs.getString("password"),
                    rs.getString("nama_user"),
                    rs.getString("id_level")
                };
                tabel.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        textIDUser.setText("");
        textUsername.setText("");
        textNama.setText("");
        textPassword.setText("");
    } 
