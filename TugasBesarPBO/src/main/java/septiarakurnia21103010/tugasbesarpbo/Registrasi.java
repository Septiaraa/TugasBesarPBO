    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        try {
            user us = new user();
            this.ps = k.getConnection().prepareStatement("update user set username=?, password=?,nama_user=?, id_level=? where id_user=?");
            ps.setString(1, us.username);
            ps.setString(2,us.password);
            ps.setString(3,us.nama_user);
            ps.setInt(4,us.id_level);
            ps.setString(5, textIDUser.getText());
            ps.executeUpdate();
            refreshTable(); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

