    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        try {
            user us = new user();
            this.ps = k.getConnection().prepareStatement("insert into user values (?,?,?,?,?)");
            ps.setInt(1, 0);
            ps.setString(2,us.username);
            ps.setString(3,us.password);
            ps.setString(4,us.nama_user);
            ps.setInt(5, us.id_level);
            ps.executeUpdate();
            //memasukkan data dari tabel gui ke sql
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_addBtnActionPerformed

